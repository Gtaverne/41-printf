#include "ft_printf.h"

void	pf_putchar(char c, t_struct *bckt)
{
	write(1, &c, 1);
	bckt->res++;
}

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}
